package com.main;
public class Customer {
    private String name;
    private double balance;

    Customer() {
    }

    public String getName() {
        return name;
    }

    public double getBalance() {
        return balance;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setBalance(double balance) {
        this.balance = balance;
    }

  
    
    
}
